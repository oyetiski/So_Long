#include "ft_printf.h"

void	ft_int(int n, int *num)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*num += 11;
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		ft_char('-', num);
	}
	if (n > 9)
	{
		ft_int(n / 10, num);
		ft_int(n % 10, num);
	}
	else
		ft_char(n + 48, num);
}
